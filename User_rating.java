package sample4;

public class User_rating {
	private String aggregate_rating;
	 private String rating_text;
	 private String rating_colour;
	 private String votes;
	public String getAggregate_rating() {
		return aggregate_rating;
	}
	public void setAggregate_rating(String aggregate_rating) {
		this.aggregate_rating = aggregate_rating;
	}
	public String getRating_text() {
		return rating_text;
	}
	public void setRating_text(String rating_text) {
		this.rating_text = rating_text;
	}
	public String getRating_colour() {
		return rating_colour;
	}
	public void setRating_colour(String rating_colour) {
		this.rating_colour = rating_colour;
	}
	public String getVotes() {
		return votes;
	}
	public void setVotes(String votes) {
		this.votes = votes;
	}

}
